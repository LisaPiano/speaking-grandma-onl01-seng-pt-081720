def speak_to_grandma(speak)
  if
    speak == "I LOVE YOU GRANDMA!"
    return "I LOVE YOU TOO PUMPKIN!"

  elsif
    speak == speak.downcase
    return "HUH? SPEAK UP, SONNY!"

  elseif
  speak == speak.capitalize
  return "NO, NOT SINCE 1938!"

else
speak == speak.upcase
return "NO, NOT SINCE 1938!"




end
end
